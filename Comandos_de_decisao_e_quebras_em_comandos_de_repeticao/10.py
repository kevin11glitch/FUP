# Escreva um programa que leia um inteiro entre 1 e 7 e imprima o dia da semana correspondente a este numero. Isto Ã©, domingo se 1, segunda-feira se 2, e assim por diante.

dia = int(input())

if dia == 1:
    print("Domingo")
if dia == 2:
    print("Segunda-feira")
if dia == 3:
    print("Terca-feira")
if dia == 4:
    print("Quarta-feira")
if dia == 5:
    print("Quinta-feira")
if dia == 6:
    print("Sexta-feira")
if dia == 7:
    print("Sabado")